package Operators;

public class AssignmentO4 {

	public static void main(String[] args) {
		
		int num=20;
		
		System.out.println("the result is:"+(num +=5));
		System.out.println("the result is:"+(num -=3));
		System.out.println("the result is:"+(num *=2));
		System.out.println("the result is:"+(num /=4));
		System.out.println("the result is:"+(num %=3));

	}

}
